package com.company.consumer;

public class CollectionConsumer {

    public static void main(String[] args) {
        byte b = 5;
        switch (b) {
            case 5:
                System.out.print("case 5 ");
            case 7:
                System.out.print("case 7 ");
                break;
            default:
                System.out.print("default");
                break;
        }
    }


}
