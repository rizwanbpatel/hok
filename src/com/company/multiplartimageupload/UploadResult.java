package com.company.multiplartimageupload;

public class UploadResult {
    String result;
    String msg;


    public UploadResult(String result, String msg) {
        this.result = result;
        this.msg = msg;
    }

    public String getResult() {
        return result;
    }


    public String getMsg() {
        return msg;
    }


}
